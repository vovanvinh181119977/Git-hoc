public class Circle {
     double banhKinh ;
    //tinh dien tich  hinh tron
    public double area() {
        return banhKinh * banhKinh * Math.PI;
    }
    //tinh chi vi hinh tron
       public double primeter() {
        return 2 * Math.PI * banhKinh;
    }

}
